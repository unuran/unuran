/*---------------------------------------------------------------------------*/

int main()
{
  UNUR_DISTR *distr;
  UNUR_GEN *gen;
  double fpm[] = { -29.1872470639646, 0.217061228413259 };
  distr = unur_distr_normal(fpm,2);
  gen = unur_init( unur_tdr_new (distr) );
  //   unurgen(gen,out,"normal_3");
  unur_distr_free(distr);
  unur_free(gen);

  exit (0);
}

